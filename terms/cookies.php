<h3>What are cookies?</h3>
Cookies are small text files that are stored on your computer's hard disk when you visit certain websites. At Trendy Publishing we may use similar techniques, such as pixels, web beacons etc. For consistency's sake, all these techniques combined will be named 'cookies'.

<h3>Why are these cookies used?</h3>
Cookies may be used for many different purposes. For example, cookies can be used to show that you have visited our website before and to identify which parts of the site you might be most interested in. Cookies can also improve your online experience by storing your preferences during your visit to our website.

<h4>Cookies from third parties</h4>
Third parties (external to Trendy Publishing) may also store cookies on your computer during your visit to Philips websites. These indirect cookies are similar to direct cookies but come from a different domain (non-Trendy Publishing) to the one you are visiting.
